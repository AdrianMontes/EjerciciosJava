package Ejercicio201;

public class Ejercicio201 {

	public static void main(String[] args) {
		int numero = 25;
		System.out.println(numero);
		
		numero += 5;
		System.out.println(numero);
		
		numero -= 15;
		System.out.println(numero);
		
		numero *= 2;
		System.out.println(numero);
		
		numero /= 2;
		System.out.println(numero);
		
		numero %= 3;
		System.out.println(numero);
	}

}
