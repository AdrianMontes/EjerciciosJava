package Ejercicio195;

public class Ejercicio195 {

	public static void main(String[] args) {
		//10 - 5
		int resta1 = 10;
		int resta2 = 5;
		int resultado = resta1 - resta2;
		System.out.println(resta1 + " - " + resta2 + " = " + resultado);
		
		//55 + 45
		int suma1 = 55;
		int suma2 = 45;
		System.out.println(suma1 + " + " + suma2 + " = " + (suma1 + suma2));
		
		//3 a negativo
		int negativo = 3 * -1;
		System.out.println(negativo);
		
		//5 x 5
		System.out.println("5 x 5 = " + (5 * 5));
		
		//25 / 7 entero
		System.out.println(25 / 7);
		
		//25 / 7 decimal
		System.out.println(25.0 / 7.0);
		
		//25 % 4
		System.out.println(25 % 4);
	}

}
